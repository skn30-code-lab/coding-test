def solution(a, b):
    # ліҖмҲҳлӘ…мқ„ month_daysлЎң ліҖкІҪн•ҳкі , KeyлҘј мІҳмқҢл¶Җн„° м •мҲҳнҳ•мңјлЎң м •мқҳ
    month_days = {
        1: 31, 2: 29, 3: 31, 4: 30, 5: 31, 6: 30,
        7: 31, 8: 31, 9: 30, 10: 31, 11: 30, 12: 31
    }
    week = ["FRI", "SAT", "SUN", "MON", "TUE", "WED", "THU"]
    
    total_days = 0
    # aмӣ” 'мқҙм „' лӢ¬л“Өмқҳ мқјмҲҳл§Ң лҲ„м Ғн•ҳм—¬ лҚ”н•Ё
    for month, day in month_days.items():
        if month < a:
            total_days += day
            
    # bмқјмқ„ лҚ”н•ҳкі  мӢңмһ‘мқј мЎ°м •мқ„ мң„н•ҙ 1мқ„ ләҢ
    total_days += b - 1
    
    return week[total_days % 7]

